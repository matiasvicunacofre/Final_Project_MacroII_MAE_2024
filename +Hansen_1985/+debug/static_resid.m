function [lhs, rhs] = static_resid(y, x, params)
T = NaN(3, 1);
lhs = NaN(9, 1);
rhs = NaN(9, 1);
T(1) = y(6)^params(3);
T(2) = y(8)*T(1);
T(3) = y(5)^(1-params(3));
lhs(1) = 1/y(1);
rhs(1) = params(1)*1/y(1)*(y(3)+1-params(2));
lhs(2) = (1-params(3))*y(4)/y(5);
rhs(2) = y(1)*params(8);
lhs(3) = y(1);
rhs(3) = y(4)+(1-params(2))*y(6)-y(6);
lhs(4) = y(6);
rhs(4) = (1-params(2))*y(6)+y(7);
lhs(5) = y(4);
rhs(5) = T(2)*T(3);
lhs(6) = y(3);
rhs(6) = params(3)*y(4)/y(6);
lhs(7) = y(2);
rhs(7) = (1-params(3))*y(4)/y(5);
lhs(8) = log(y(8));
rhs(8) = log(y(8))*params(4)+x(1);
lhs(9) = y(9);
rhs(9) = y(4)/y(5);
end
