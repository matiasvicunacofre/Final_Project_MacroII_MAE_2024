function [lhs, rhs] = static_resid(y, x, params)
T = NaN(11, 1);
lhs = NaN(18, 1);
rhs = NaN(18, 1);
T(1) = exp(y(6))^(params(7)-1);
T(2) = exp(y(5))^(params(7)-1);
T(3) = params(9)*exp(y(6))^params(7)+(1-params(9))*exp(y(5))^params(7);
T(4) = exp(y(12))*exp(y(10))^params(1);
T(5) = exp(y(17))^(1-params(1));
T(6) = T(4)*T(5);
T(7) = exp(y(11))*exp(y(9))^params(6);
T(8) = exp(y(16))^(1-params(6));
T(9) = T(7)*T(8);
T(10) = exp(y(3))*params(9)*T(1)/exp(y(4))*(1-params(1))/exp(y(17));
T(11) = exp(y(2))*(1-params(6))*T(2)*(1-params(9))/exp(y(4))/exp(y(16));
lhs(1) = params(9)*T(1)/exp(y(4));
rhs(1) = T(1)*params(9)*params(2)/exp(y(4))*(1+exp(y(14))-params(8));
lhs(2) = params(9)*T(2)/exp(y(4));
rhs(2) = T(2)*params(2)*(1-params(9))/exp(y(4))*exp(y(13));
lhs(3) = exp(y(4));
rhs(3) = T(3)^(1/params(7));
lhs(4) = exp(y(10));
rhs(4) = exp(y(3))+(1-params(8))*(exp(y(10))+exp(y(9)))-exp(y(9))-exp(y(6));
lhs(5) = exp(y(2));
rhs(5) = exp(y(5));
lhs(6) = exp(y(8));
rhs(6) = exp(y(10))+exp(y(9));
lhs(7) = exp(y(3));
rhs(7) = T(6);
lhs(8) = exp(y(2));
rhs(8) = T(9);
lhs(9) = exp(y(1));
rhs(9) = exp(y(3))+exp(y(2));
lhs(10) = exp(y(18));
rhs(10) = exp(y(17))+exp(y(16));
lhs(11) = params(10)-exp(y(18));
rhs(11) = T(10);
lhs(12) = params(10)-exp(y(18));
rhs(12) = T(11);
lhs(13) = exp(y(7));
rhs(13) = exp(y(3))-exp(y(6));
lhs(14) = exp(y(14));
rhs(14) = params(1)*exp(y(3))/exp(y(10));
lhs(15) = exp(y(13));
rhs(15) = params(6)*exp(y(2))/exp(y(9));
lhs(16) = exp(y(15));
rhs(16) = exp(y(14))+exp(y(13));
lhs(17) = y(12);
rhs(17) = y(12)*params(3)+x(2);
lhs(18) = y(11);
rhs(18) = y(11)*params(3)+x(1);
end
