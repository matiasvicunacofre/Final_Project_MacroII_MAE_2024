function [lhs, rhs] = static_resid(y, x, params)
T = NaN(5, 1);
lhs = NaN(9, 1);
rhs = NaN(9, 1);
T(1) = exp(y(5))^(-1);
T(2) = exp(y(4))*exp(y(3))^params(1);
T(3) = exp(y(6))^(1-params(1));
T(4) = T(2)*T(3);
T(5) = exp(y(1))/exp(y(6));
lhs(1) = T(1);
rhs(1) = params(2)*T(1)*(1+exp(y(8))-params(5));
lhs(2) = exp(y(3));
rhs(2) = exp(y(1))+exp(y(3))*(1-params(5))-exp(y(5));
lhs(3) = exp(y(1))*(1-params(1))/exp(y(6));
rhs(3) = exp(y(5))*params(6)/(1-exp(y(6)));
lhs(4) = exp(y(1));
rhs(4) = T(4);
lhs(5) = exp(y(7));
rhs(5) = (1-params(1))*T(5);
lhs(6) = exp(y(8));
rhs(6) = params(1)*exp(y(1))/exp(y(3));
lhs(7) = exp(y(2));
rhs(7) = exp(y(1))-exp(y(5));
lhs(8) = exp(y(9));
rhs(8) = T(5);
lhs(9) = y(4);
rhs(9) = y(4)*params(3)+x(1);
end
